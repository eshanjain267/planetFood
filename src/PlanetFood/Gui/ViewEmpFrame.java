    private void btnDoTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTaskActionPerformed
        if(btnViewSingleProduct.isSelected())
        {
            this.dispose();
            SingleEmpDetailsFrame Emps = new SingleEmpDetailsFrame();
            Emps.setVisible(true);

        }
        else if(btnViewAllProduct.isSelected())
        {

            AllEmpDetailsFrame EmpAll=new AllEmpDetailsFrame();
            EmpAll.setVisible(true);
            this.dispose();

        }
        else
        {
            JOptionPane.showConfirmDialog(null,"Make a Choice","Choice!",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDoTaskActionPerformed

